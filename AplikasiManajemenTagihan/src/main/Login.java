    // Untuk ke halaman dashboard
    private void btnMasukActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMasukActionPerformed
        String username = txtNamaPengguna.getText();
        String password = new String(txtKataSandi.getPassword());

        if (username.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Username atau password tidak boleh kosong");
            return;
        }

        try (Connection conn = Koneksi.getConnection()) {
            if (conn == null) {
                JOptionPane.showMessageDialog(this, "Koneksi ke database gagal.");
                return;
            }

            String sql = "SELECT id, username FROM akun WHERE username = ? AND password = ?";
            try (PreparedStatement stmt = conn.prepareStatement(sql)) {
                stmt.setString(1, username);
                stmt.setString(2, hashPassword(password)); // âœ… Ini wajib!

                try (ResultSet rs = stmt.executeQuery()) {
                    if (rs.next()) {
                        String user = rs.getString("username");
                        JOptionPane.showMessageDialog(this, "Login berhasil. Selamat datang, " + user);
                        this.dispose();
                        new Dashboard().setVisible(true);
                    } else {
                        JOptionPane.showMessageDialog(this, "Username atau password salah.");
                    }
                }
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan database: " + e.getMessage());
        }
    }//GEN-LAST:event_btnMasukActionPerformed
    
