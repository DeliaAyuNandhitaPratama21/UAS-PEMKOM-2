    // Untuk memanggil saat tombol Edit diklik
    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        int selectedRow = tabelTagihan.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih data yang ingin diedit!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int rowIndex = tabelTagihan.convertRowIndexToModel(selectedRow);
        String namaTagihan = tabelTagihan.getValueAt(rowIndex, 1).toString();

        try {
            Connection conn = Koneksi.getConnection();
            String sql = "SELECT * FROM tagihan WHERE nama_tagihan = ?";
            PreparedStatement stmt = conn.prepareStatement(sql);
            stmt.setString(1, namaTagihan);
            ResultSet rs = stmt.executeQuery();

            if (rs.next()) {
                int idTagihan = rs.getInt("id"); // gunakan nama kolom yang benar dari database
                EditTagihan editDialog = new EditTagihan(this, true, idTagihan);
                editDialog.setDataTagihan(
                        idTagihan,
                        rs.getString("nama_tagihan"),
                        rs.getDouble("jumlah"),
                        rs.getDate("jatuh_tempo"),
                        rs.getString("kategori"),
                        rs.getString("status")
                );
                editDialog.setVisible(true);
                loadDataToTable(); // refresh data setelah edit
            } else {
                JOptionPane.showMessageDialog(this, "Data tidak ditemukan!", "Error", JOptionPane.ERROR_MESSAGE);
            }

            rs.close();
            stmt.close();
            conn.close();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat mengambil data!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEditActionPerformed

    // Untuk menghapus data tagihan yang dipilih dari tabel dan database
    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        int selectedRow = tabelTagihan.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih data yang ingin dihapus!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this,
                "Apakah Anda yakin ingin menghapus tagihan ini?",
                "Konfirmasi Hapus",
                JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            int rowIndex = tabelTagihan.convertRowIndexToModel(selectedRow);
            String namaTagihan = tabelTagihan.getValueAt(rowIndex, 1).toString();

            try {
                Connection conn = Koneksi.getConnection();
                String sql = "DELETE FROM tagihan WHERE nama_tagihan = ?";
                PreparedStatement stmt = conn.prepareStatement(sql);
                stmt.setString(1, namaTagihan);
                int result = stmt.executeUpdate();

                if (result > 0) {
                    JOptionPane.showMessageDialog(this, "Data berhasil dihapus!", "Sukses", JOptionPane.INFORMATION_MESSAGE);
                    loadDataToTable(); 
                } else {
                    JOptionPane.showMessageDialog(this, "Gagal menghapus data!", "Error", JOptionPane.ERROR_MESSAGE);
                }

                stmt.close();
                conn.close();
            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat menghapus data!", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnHapusActionPerformed

