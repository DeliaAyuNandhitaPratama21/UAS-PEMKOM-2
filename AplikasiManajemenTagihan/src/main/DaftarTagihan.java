    // Untuk mengambil semua data tagihan dari database
    // dan menampilkannya ke dalam tabel di halaman daftar tagihan  
    private void loadDataToTable() {
    DefaultTableModel model = (DefaultTableModel) tabelTagihan.getModel();
    model.setRowCount(0); 

    String query = "SELECT * FROM tagihan";

    try (
        Connection conn = Koneksi.getConnection();
        PreparedStatement stmt = conn.prepareStatement(query);
        ResultSet rs = stmt.executeQuery();
    ) {
        int no = 1;
        while (rs.next()) {
            model.addRow(new Object[]{
                no++,
                rs.getString("nama_tagihan"),
                rs.getDouble("jumlah"),
                rs.getDate("jatuh_tempo"),
                rs.getString("kategori"),
                rs.getString("status")
            });
        }
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Gagal memuat data dari database.");
    }
}

